// Операторы присваивания

let age = 18;
age += 2; // age = age + 2
age -= 3; // age = age - 3
age *= 3; // age = age * 3
age /= 3; // age = age / 3

age++; // age = age + 1
age--; // age = age - 1

console.log(age);

// Операторы сравнения

const vasyaAge = 20;
console.log(age >  vasyaAge);
console.log(age >= vasyaAge);
console.log(age <  vasyaAge);
console.log(age <= vasyaAge);
console.log(age == vasyaAge);

